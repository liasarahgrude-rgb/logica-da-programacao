#Pergunte ao usuário se ele está ativo no sistema (resposta: sim ou nao). 
#Se ele digitar “sim”, mostre que o usuário está ativo, senão mostre que está inativo.
ativo = input ("Você está ativo no sistema? Responda com sim ou não: ")
if ativo == "sim":
    print (f"O usuário está ativo")
else: print (f"O usuário não está ativo")